class Switch{
    public static void main(String[] args){
        String day ="Wed";
        switch(day){
            case "Sun":
                System.out.println("Today is Sunday");
                break;
            case "Mon":
                System.out.println("Today is Monday");
                break;
            case "Wed":
                System.out.println("Today is Wednesday");
                break;
        }
    }
}